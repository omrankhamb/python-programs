""" Loop in python"""

# Range built in function
# range(start, end, step)

print("Range function")
for i in range(1,10,1):
    print(i,end=" ")
print()

print("In different way : ")
for i in range(10):
    print(i,end=" ")
print()

print("Reverse the range : ")
for i in range(10,1,-1):
    print(i,end=" ")

