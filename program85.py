"""
    Write a Python program to read 2 numbers from user. Display the result using bitwise and, bitwise or, bitwise xor, right shift and left shift operator
"""

iNo,iNo2 = map(int,input("Enter Number : ").split(","))

print(f"Bitwise OR {iNo | iNo2}")
print(f"Bitwise XOR {iNo ^ iNo2}")
print(f"Bitwise OR {iNo << iNo2}")
print(f"Bitwise OR {iNo >> iNo2}")






